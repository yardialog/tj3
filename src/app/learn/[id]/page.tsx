// Компонент для рендеринга блока контента
function ContentBlock({ block }: { block: ContentBlock }) {
  const getIcon = () => {
    switch (block.type) {
      case 'tips': return <Lightbulb className="h-5 w-5 text-yellow-500" />;
      case 'warning': return <AlertTriangle className="h-5 w-5 text-red-500" />;
      case 'checklist': return <CheckSquare className="h-5 w-5 text-green-500" />;
      case 'example': return <Info className="h-5 w-5 text-blue-500" />;
      case 'important': return <Info className="h-5 w-5 text-purple-500" />;
      default: return null;
    }
  };

  const getStyle = () => {
    switch (block.type) {
      case 'tips': return 'bg-yellow-50 dark:bg-yellow-900/20 border-yellow-200 dark:border-yellow-800';
      case 'warning': return 'bg-red-50 dark:bg-red-900/20 border-red-200 dark:border-red-800';
      case 'checklist': return 'bg-green-50 dark:bg-green-900/20 border-green-200 dark:border-green-800';
      case 'example': return 'bg-blue-50 dark:bg-blue-900/20 border-blue-200 dark:border-blue-800';
      case 'important': return 'bg-purple-50 dark:bg-purple-900/20 border-purple-200 dark:border-purple-800';
      default: return '';
    }
  };

  if (block.type === 'text') {
    return (
      <p className="text-muted-foreground leading-relaxed text-base mb-4">
        {block.content}
      </p>
    );
  }

  return (
    <div className={`rounded-lg border p-4 mb-4 ${getStyle()}`}>
      {block.title && (
        <div className="flex items-center gap-2 mb-3">
          {getIcon()}
          <h4 className="font-semibold text-sm">{block.title}</h4>
        </div>
      )}
      {block.content && (
        <p className="text-sm text-muted-foreground mb-3">{block.content}</p>
      )}
      {block.items && (
        <ul className="space-y-2">
          {block.items.map((item, index) => (
            <li key={index} className="text-sm flex items-start gap-2">
              {block.type === 'checklist' ? (
                <span className="text-green-500 mt-0.5">✓</span>
              ) : block.type === 'warning' ? (
                <span className="text-red-500 mt-0.5">•</span>
              ) : (
                <span className="text-muted-foreground mt-0.5">•</span>
              )}
              <span>{item}</span>
            </li>
          ))}
        </ul>
      )}
    </div>
  );
}

